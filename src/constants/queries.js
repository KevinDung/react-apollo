import gql from 'graphql-tag';

// store query
// gpl function is used to parse the plain string that contains the GraphQL code
const FEED_QUERY = gql`
  # first : limit of element
  # skip : offset where the query will start
  # orderBy : how to sort
  query FeedQuery($first: Int, $skip: Int, $orderBy: LinkOrderByInput) {
    feed(first: $first, skip: $skip, orderBy: $orderBy) {
      links {
        id
        createdAt
        url
        description
        postedBy {
          id
          name
        }
        votes {
          id
          user {
            id
          }
        }
      }
      count
    }
  }
`;

const FEED_SEARCH_QUERY = gql`
  query FeedSearchQuery($filter: String!) {
    feed(filter: $filter) {
      links {
        id
        url
        description
        createdAt
        postedBy {
          id
          name
        }
        votes {
          id
          user {
            id
          }
        }
      }
    }
  }
`;

export { FEED_QUERY, FEED_SEARCH_QUERY };
